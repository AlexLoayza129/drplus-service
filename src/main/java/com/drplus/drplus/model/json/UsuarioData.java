package com.drplus.drplus.model.json;

public class UsuarioData {
    Integer id;
    Integer usertype;
    String username;
    String password;
    String nombre;
    String apellido;
    String direccion;
    String telefono;
    
    public Integer getId() {
        return id;
    }
    public Integer getUsertype() {
        return usertype;
    }
    public String getUsername() {
        return username;
    }
    public String getPassword() {
        return password;
    }
    public String getNombre() {
        return nombre;
    }
    public String getApellido() {
        return apellido;
    }
    public String getDireccion() {
        return direccion;
    }
    public String getTelefono() {
        return telefono;
    }

   
    

}
